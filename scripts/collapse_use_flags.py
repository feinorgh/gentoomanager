#!/usr/bin/env python3
"""
collapse_use_flags.py
~~~~~~~~~~~~~~~~~~~~~
Reads per-host USE-flag JSON facts collected by the collect_use_flags Ansible
role, then collapses them into group_vars/all/, group_vars/<group>/, and
host_vars/<host>/ YAML files using a greedy, least-specific-first strategy:

  1. If a flag/value is identical for EVERY host → group_vars/all/
  2. Else for each group (sorted largest-first) where all members agree →
       group_vars/<group>/
  3. Remaining host-unique flags → host_vars/<host>/

Domains handled:
  • global_use_flags  (USE="..." in make.conf)
  • use_expand        (VIDEO_CARDS, INPUT_DEVICES, CPU_FLAGS_X86, …)
  • build_profile     (CFLAGS, CXXFLAGS, LDFLAGS, MAKEOPTS, FEATURES)
  • package_use       (/etc/portage/package.use/*)
  • make_conf         (all remaining variable assignments in make.conf)
  • use_flag_types    (classifies global_use_flags as global/local/unknown)

Usage:
    python3 scripts/collapse_use_flags.py \\
        --facts-dir /tmp/ansible_use_flags_facts \\
        --output-dir /path/to/project \\
        [--dry-run] [--update]
"""

from __future__ import annotations

import argparse
import difflib
import json
import re
import sys
from collections import defaultdict
from pathlib import Path
from typing import Any

try:
    import yaml
except ImportError:
    print("ERROR: PyYAML is required.  Install it with:  pip install pyyaml", file=sys.stderr)
    sys.exit(1)

import re as _re
_SAFE_NAME = _re.compile(r'^[a-zA-Z0-9][a-zA-Z0-9._-]{0,127}$')


def _safe_path_component(name: str, kind: str) -> str:
    """Raise ValueError if name is unsafe to use as a filesystem path component."""
    if not name or '..' in name or '/' in name or '\\' in name:
        raise ValueError(f"Unsafe {kind} name: {name!r}")
    if not _SAFE_NAME.match(name):
        raise ValueError(f"Unsafe {kind} name: {name!r}")
    return name


